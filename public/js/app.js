let hamburger__btn = document.querySelector("#hamburger__btn")
let respinsive__nav__menu = document.querySelector("#respinsive__nav__menu")
let close__btn= document.querySelector("#close__btn")
hamburger__btn.addEventListener("click" , function(){
    respinsive__nav__menu.classList.add("pt-4")
    respinsive__nav__menu.classList.remove("h-0")
    close__btn.classList.add("flex")
    close__btn.classList.remove("hidden")
    hamburger__btn.classList.remove("flex")
    hamburger__btn.classList.add("hidden")
})
close__btn.addEventListener("click" , function(){
    respinsive__nav__menu.classList.remove("pt-4")
    respinsive__nav__menu.classList.add("h-0")
    close__btn.classList.remove("flex")
    close__btn.classList.add("hidden")
    hamburger__btn.classList.add("flex")
    hamburger__btn.classList.remove("hidden")
})